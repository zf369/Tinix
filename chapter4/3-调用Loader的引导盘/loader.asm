; ==========================================
; loader.asm
; 简单的Loader，打印一个字符
; ==========================================

; 需要booter将它复制到内存0100h处，放在这个地址的好处是，也可以编译成com文件进行调试
org    0100h    

    mov    ax, 0B800h
    mov    gs, ax

    mov    ah, 5ah                         ; 0000: 黑底    1111: 白字
    mov    al, 'F'

    mov    [gs:((80 * 0 + 39) * 2)], ax    ; 屏幕第 0 行, 第 39 列。

    jmp    $